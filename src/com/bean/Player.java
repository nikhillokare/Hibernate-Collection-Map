package com.bean;

public class Player {

	   private int jno;
	   private String player_name;
	   private float player_sold_value;
	   
	  //Getters And Setters 
	public int getJno() {
		return jno;
	}
	public void setJno(int jno) {
		this.jno = jno;
	}
	public String getPlayer_name() {
		return player_name;
	}
	public void setPlayer_name(String player_name) {
		this.player_name = player_name;
	}
	public float getPlayer_sold_value() {
		return player_sold_value;
	}
	public void setPlayer_sold_value(float player_sold_value) {
		this.player_sold_value = player_sold_value;
	}
	   
	   
}
